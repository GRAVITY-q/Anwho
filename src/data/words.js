export const getRandomWord = (categoryId) => {
  const category = CATEGORIES.find(c => c.id === categoryId);
  if (!category) return null;
  const randomIndex = Math.floor(Math.random() * category.words.length);
  return category.words[randomIndex];
};

export const getVagueHint = (categoryId) => {
  const category = CATEGORIES.find(c => c.id === categoryId);
  if (!category || !category.hints) return "Related to the category.";
  const randomIndex = Math.floor(Math.random() * category.hints.length);
  return category.hints[randomIndex];
};
